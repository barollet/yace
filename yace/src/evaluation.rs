fn material_evaluation<const TO_MOVE: Color>(board: &Board) -> i16 {
    let mut score = 0;
    for piece in [PAWN, KNIGHT, BISHOP, ROOK, QUEEN, KING] {
        let pieces = board.bitboards[piece];
        let piece_diff = (pieces & board.pieces[TO_MOVE]).count_ones() - (pieces & board.pieces[!TO_MOVE]).count_ones();
        score += (piece_diff * piece.value() as u32) as i16;
    }
    score
}